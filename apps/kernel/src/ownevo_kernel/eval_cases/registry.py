"""Eval-case write/read API (W2.3).

Eval cases are how the regression gate decides whether a skill change
is an improvement or a regression. Every case has a `provenance` —
how it got into the suite — which is the audit trail for "why is this
test here":

  hand-authored        — seeded by humans (W2 bootstrap)
  cluster-derived      — promoted from a failure cluster
  nl-gen               — generated by the NL-gen pipeline (Phase 2)
  retention-violation  — generated by walking a skill's retention contract
  rejected-feedback    — captured from a human "reject + comment" decision

This module is the CRUD seam. The gate runner reads via `list_eval_cases`;
clustering and NL-gen pipelines write via `add_eval_case`.
"""

from __future__ import annotations

import json
import math
from typing import Any
from uuid import UUID

import asyncpg

from ..types import EvalCase, ProvenanceKind


async def add_eval_case(
    conn: asyncpg.Connection,
    *,
    provenance: ProvenanceKind | str,
    input: dict[str, Any],
    expected_behavior: dict[str, Any],
    workflow_id: str | None = None,
    cluster_id: UUID | None = None,
    regression_tolerance: float | None = None,
    is_test_fold: bool = False,
) -> EvalCase:
    """Insert one eval case and return the typed model.

    `provenance` accepts the enum or its string value.
    `regression_tolerance` is the per-case fractional slip the gate
    tolerates (0.05 = 5pp); leave None to use the gate's default.
    `is_test_fold=True` marks this as a held-out evaluation case the
    gate refuses to use for training (train/test discipline).
    """
    prov = provenance.value if isinstance(provenance, ProvenanceKind) else provenance
    try:
        input_json = json.dumps(input)
        expected_json = json.dumps(expected_behavior)
    except TypeError as exc:
        field = "input" if "input" in str(exc) else "expected_behavior"
        raise TypeError(f"add_eval_case: {field} is not JSON-serializable: {exc}") from exc
    row = await conn.fetchrow(
        """
        INSERT INTO eval_cases (
            workflow_id, provenance, cluster_id,
            input, expected_behavior, regression_tolerance, is_test_fold
        )
        VALUES ($1, $2::provenance_kind, $3, $4::jsonb, $5::jsonb, $6, $7)
        RETURNING id, workflow_id, provenance::text AS provenance, cluster_id,
                  input, expected_behavior, regression_tolerance, is_test_fold,
                  created_at
        """,
        workflow_id,
        prov,
        cluster_id,
        input_json,
        expected_json,
        regression_tolerance,
        is_test_fold,
    )
    return _row_to_case(row)


async def get_eval_case(conn: asyncpg.Connection, eval_case_id: UUID) -> EvalCase | None:
    row = await conn.fetchrow(
        """
        SELECT id, workflow_id, provenance::text AS provenance, cluster_id,
               input, expected_behavior, regression_tolerance, is_test_fold,
               created_at
        FROM eval_cases
        WHERE id = $1
        """,
        eval_case_id,
    )
    return _row_to_case(row) if row is not None else None


async def list_eval_cases(
    conn: asyncpg.Connection,
    *,
    workflow_id: str | None = None,
    provenance: ProvenanceKind | str | None = None,
    is_test_fold: bool | None = None,
    cluster_id: UUID | None = None,
    limit: int | None = None,
) -> list[EvalCase]:
    """Filter by any combination of fields. No filter = full table.

    Ordered by `created_at ASC` so the gate runner sees cases in
    promotion order — older cases (more likely the load-bearing ones)
    fail-fast first.

    `limit` caps the result set (e.g. before pushing to an external API).
    No limit = all matching rows (safe for the gate runner; callers that
    push to upstream APIs should pass an explicit cap).
    """
    clauses: list[str] = []
    params: list[Any] = []

    if workflow_id is not None:
        params.append(workflow_id)
        clauses.append(f"workflow_id = ${len(params)}")
    if provenance is not None:
        params.append(
            provenance.value if isinstance(provenance, ProvenanceKind) else provenance,
        )
        clauses.append(f"provenance = ${len(params)}::provenance_kind")
    if is_test_fold is not None:
        params.append(is_test_fold)
        clauses.append(f"is_test_fold = ${len(params)}")
    if cluster_id is not None:
        params.append(cluster_id)
        clauses.append(f"cluster_id = ${len(params)}")

    where = f"WHERE {' AND '.join(clauses)}" if clauses else ""
    limit_clause = ""
    if limit is not None:
        params.append(limit)
        limit_clause = f"LIMIT ${len(params)}"
    rows = await conn.fetch(
        f"""
        SELECT id, workflow_id, provenance::text AS provenance, cluster_id,
               input, expected_behavior, regression_tolerance, is_test_fold,
               created_at
        FROM eval_cases
        {where}
        ORDER BY created_at ASC, id ASC
        {limit_clause}
        """,
        *params,
    )
    return [_row_to_case(r) for r in rows]


# ---------------------------------------------------------------------------
# Internals
# ---------------------------------------------------------------------------


def _row_to_case(row: asyncpg.Record) -> EvalCase:
    def _decode(v: Any) -> Any:
        return json.loads(v) if isinstance(v, str) else v

    tol = row["regression_tolerance"]
    tol_float: float | None = None
    if tol is not None:
        tol_float = float(tol)
        if not math.isfinite(tol_float):
            raise ValueError(f"regression_tolerance is not finite: {tol_float}")
    return EvalCase(
        id=row["id"],
        workflow_id=row["workflow_id"],
        provenance=ProvenanceKind(row["provenance"]),
        cluster_id=row["cluster_id"],
        input=_decode(row["input"]),
        expected_behavior=_decode(row["expected_behavior"]),
        regression_tolerance=tol_float,
        is_test_fold=row["is_test_fold"],
        created_at=row["created_at"],
    )
